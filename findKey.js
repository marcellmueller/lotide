const findKey = (object, callback) => {
  for (const each in object) {
    if (callback(object[each]) === true) {
      return each;
    }
  }
  return false;
};

findKey(
  {
    'Blue Hill': { stars: 1 },
    Akaleri: { stars: 3 },
    noma: { stars: 2 },
    elBulli: { stars: 3 },
    Ora: { stars: 2 },
    Akelarre: { stars: 3 },
  },
  (x) => x.stars === 2
); // => "noma"

